# This is a Band Name Generator

# Print a welcome message to the user.
print("Welcome to the Band name generator.")

# Prompt the user to input the city they grew up in and store it in the variable 'city'.
city = input("Which city did you grow up in?\n")

# Prompt the user to input the name of their pet and store it in the variable 'pet'.
pet = input("What is the name of your pet?\n")

# Combine the city and pet name to create a band name and print the result.
print("Your band name could be " + city + " " + pet)
